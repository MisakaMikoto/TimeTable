public class Subject {
    /**
     * The Name.
     */
    String name;
    /**
     * The Credit.
     */
    String credit;

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets credit.
     *
     * @return the credit
     */
    public String getCredit() {
        return credit;
    }

    /**
     * Sets credit.
     *
     * @param credit the credit
     */
    public void setCredit(String credit) {
        this.credit = credit;
    }
}
